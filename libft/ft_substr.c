#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	char	*str;

	i = 0;
	if (!s || !*s || start >= ft_strlen(s) || len < 1 || len > 2147483647)
		return (ft_strdup(""));
	if (!(str = malloc(sizeof(char) * len + 1)))
		return (NULL);
	while (i < len && s[start + i])
	{
		str[i] = s[start + i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
